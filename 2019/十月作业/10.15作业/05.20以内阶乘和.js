// 5、求1!+2!+3!+...+20!的和
     var he = 0
        //遍历1-20之间的数字
        for (var i = 1; i <= 20; i++) {
            //定义变量计算阶乘切乘积每次循环之后都能重新计算洗一个数的阶乘
            var cheng = 1;
            //遍历i之前的所有数字用以计算阶乘和
            for (var j = 1; j <=i; j++) {
                //计算阶乘
                cheng*=j
            }
            //循环结束累加和
            he+=cheng
        }
        console.log('1!+2!+3!+...+20!的和为'+he)
