// 5.  求出数组var arr = [1, 2, 4, 5, 10, 100, 2, -22]中最小元素的索引，
// 如果这样的元素个数大于1，则返回索引最小的数的索引
// 定义数组
var arr = [1, 2, 4, 5, 10, 100, 2, -22,]
// 假设最小值为第一个元素  定义一个变量为0用来接受下标
min = arr[0],minindex = 0;;
// 循环数组元素
for (var i = 0; i < arr.length; i++) {
   // 判断如果后面元素小于定义的最小值
   if (min > arr[i]) {
      // 则把值赋给定义的最小值
      min = arr[i]
      // 使变量等于最小值的下标
      minindex = i;
   }
}
// 打印
console.log(min+'小标为:'+minindex)